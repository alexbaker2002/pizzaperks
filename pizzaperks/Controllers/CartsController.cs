using Microsoft.AspNetCore.Authorization;
using Microsoft.AspNetCore.Identity;
using Microsoft.AspNetCore.Mvc;
using Microsoft.EntityFrameworkCore;
using pizzaperks.Data;
using pizzaperks.Models;
using pizzaperks.Services.Interfaces;

namespace pizzaperks.Controllers
{
    [Authorize]
    public class CartController(ApplicationDbContext context, UserManager<PZUser> userManager, ICartService cartService) : Controller
    {
        private readonly ApplicationDbContext _context = context;
        private readonly UserManager<PZUser> _userManager = userManager ?? null!;
        private readonly ICartService _cartService = cartService ?? null!;


        // GET: User Cart with Items

        public async Task<IActionResult> Index()
        {

            if (_userManager == null)
            {
                return NotFound();
            }
            PZUser? user = await _userManager.GetUserAsync(User);

            if (user is null)
            {
                return View(null);
            }
            int cartId = user!.CartId;

            Cart cart = await _cartService.GetCartWithItemsAsync(user);


            return View(cart.Products);
        }


        public async Task<IActionResult> Create()
        {
            PZUser? user = await _userManager.GetUserAsync(User);
            if (user is null)
            {
                return BadRequest();
            }
            Cart cart = await _cartService.GetCartWithItemsAsync(user);


            return View(cart);
        }

        // GET: Carts/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var cart = await _context.Carts
                .FirstOrDefaultAsync(m => m.Id == id);
            if (cart == null)
            {
                return NotFound();
            }

            return View(cart);
        }


        // GET: Carts/Edit/5
        public async Task<IActionResult> Edit(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var cart = await _context.Carts.FindAsync(id);
            if (cart == null)
            {
                return NotFound();
            }
            return View(cart);
        }

        // POST: Carts/Edit/5
        // To protect from overposting attacks, enable the specific properties you want to bind to.
        // For more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Edit(int id, [Bind("Id")] Cart cart)
        {
            if (id != cart.Id)
            {
                return NotFound();
            }

            if (ModelState.IsValid)
            {
                try
                {
                    _context.Update(cart);
                    await _context.SaveChangesAsync();
                }
                catch (DbUpdateConcurrencyException)
                {
                    if (!CartExists(cart.Id))
                    {
                        return NotFound();
                    }
                    else
                    {
                        throw;
                    }
                }
                return RedirectToAction(nameof(Index));
            }
            return View(cart);
        }




        // POST: Carts/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> DeleteConfirmed(Product product)
        {
            int id = 0;
            // delete above line
            //get users cart and remove product from it,
            // return new list of products

            var cart = await _context.Carts.FindAsync(id);
            if (cart != null)
            {
                _context.Carts.Remove(cart);
            }

            await _context.SaveChangesAsync();
            return RedirectToAction(nameof(Index));
        }

        private bool CartExists(int id)
        {
            return _context.Carts.Any(e => e.Id == id);
        }
    }
}
